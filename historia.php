<h3><a name="historia_kielpinka">Historia Kiełpinka</a></h3>

<h4>Okres przedwojenny</h4>

<p>W Kiełpinku istniał majątek ziemski zlokalizowany mniej więcej na terenie, na którym obecnie znajdują się budynki 
firm Femax i DMT Systems. Majątek składał się z dworu, zlokalizowanego naprzeciwko obecnego budynku nr 44, 
oraz kilku zabudowań, z których niektóre stoją do dziś.</p>

<p>Najstarsze budynki majątku:</p>
<ul>
	<li>Pierwszy budynek od strony obwodnicy, prawdopodobnie obora albo kurnik, obecnie budynek firmy Femax.</li>
	<li>Budynek mieszkalny nr 44 – podobno mieszkał w nim zarządca majątku, nadal zamieszkany.</li>
	<li>Budynki mieszkalne nr 15 i 19 <span class="sprawdzic">(sprawdzić dokładnie numerację)</span> &mdash; tzw. czworaki, 
	domy chłopów pracujących we dworze, nadal zamieszkane.</li>
	<li>Dwie stodoły, jedna między obecnym budynkiem nr 46 a ulicą Szczęśliwą, druga naprzeciwko budynku 48 w stronę obwodnicy, obie rozebrane.</li>
</ul>

<p>Później, tuż przed II wojną światową, wybudowane zostały kolejne budynki:</p>
<ul>
<li>Budynek nr 46 &mdash; świniarnia nr 1 (tzw. &#8222;jedynka&#8221;).</li>
<li>Mały budynek między 46 i 48 &mdash; kuźnia (czynna do lat 80. XX wieku).</li>
<li>Budynek nr 48 &mdash; spichlerz i stodoła.</li>
</ul>

<p>W wyniku działań wojennych dworek i część budynków uległy zniszczeniu.</p>

<h4>Okres powojenny</h4>

<h5>PGR Kiełpinek</h5>

<p>Po zakończeniu II wojny światowej majątek w Kiełpinku przeszedł na rzecz Skarbu Państwa,
prawdopodobnie zgodnie z ustaleniami dekretu z 6 września 1944 r. (reforma rolna).</p>

<p>W 1953 r. <span class="sprawdzic">(sprawdzić datę)</span> na terenie majątku utworzono <strong>PGR Kiełpinek</strong>. 
Mniej więcej w tym okresie <span class="sprawdzic">(sprawdzić)</span> powstało sześć nowych budynków w okolicy skrzyżowania
ulicy Szczęśliwej z Przytulną. Budynki były własnością PGR oraz Stoczni Gdańskiej. Zarówno dotychczas istniejące, 
jak i nowe budynki mieszkalne zostały zamieszkane przez nowych mieszkańców wsi &mdash; repatriantów (m. in. ze Lwowa) 
oraz osoby, które przyjechały na Pomorze w poszukiwaniu pracy (m. in. z Wielkopolski, Kujaw czy południa Polski).</p>

<p>W czasie istnienia PGR Kiełpinek powstały kolejne budynki:</p>
<ul>
<li>dwie świniarnie, na terenie między Femaxem, obwodnicą, linią kolejową i ul. Szczęśliwą, wraz z budynkami dodatkowymi 
(kotłownią, parownią, silosami itp.) &mdash; obecnie nieistniejące.</li>
<li>kiszarnia &mdash; obecny budynek nr 42 (pod koniec lat 70. lub 80. XX w. przerobiony na budynek mieszkalny).</li>
</ul>

<p>PGR Kiełpinek zajmował się hodowlą świń oraz uprawą ziemniaków i zbóż. Część upraw wykorzystywana była jako pasza dla świń.</p>

<h5>Zakłady Transportu Mięsnego</h5> 

<p>W okolicy roku 1970 <span class="sprawdzic">(ustalić datę)</span> PGR w Kiełpinek został zlikwidowany, a jego budynki
zostały przejęte przez inne zakłady. Na terenie, na którym obecnie znajduje się firma Femax, powstała baza transportowa
<strong>Zakładów Transportu Mięsnego</strong>. Centrala ZTM mieściła się w Gdańsku przy ulicy Angielska Grobla, natomiast 
w Kiełpinku mieścił się parking dla ciężarówek, warsztaty samochodowe i tego typu infrastruktura. Jako magazyn ZTM
służył między innymi fragment dworu, który przetrwał mniej więcej do lat 70. Baza ZTM istniała do 1992 roku
<span class="sprawdzic">(sprawdzić datę)</span>.</p>

<p>Zabudowania po drugiej stronie ulicy Szczęśliwej przez pewien czas należały do firmy Supon, która działała około
trzech lat. W roku 1981 <span class="sprawdzic">(sprawdzić datę)</span> budynki zostały przejęte przez nowoutworzoną
<strong>Spółdzielnię Produkcyjno-Rolniczą &#8222;Morena&#8221;</strong>.</p>

<h5>Spółdzielnia Produkcyjno-Rolnicza &#8222;Morena&#8221;</h5>

<p>SPR &#8222;Morena&#8221; zajmowała się początkowo uprawą pieczarek. Jest to o tyle ciekawe, że jako w jednym z pierwszych
miejsc w Polsce hodowano tutaj również grzybnię. Pieczarkarnie mieściły się w budynkach, w których za czasów PGR Kiełpinek
hodowano świnie. Nieco później SPR &#8222;Morena&#8221; uzupełniła swoją działalność o uprawę porzeczek i truskawek. Owoce te uprawiane
były na terenie, na którym znajduje się obecnie hipermarket Auchan, sklep Leroy Merlin i serwis Norauto.</p>

<p>Czasy działalności SPR &#8222;Morena&#8221; to okres świetności Kiełpinka. Wieś dzięki autobusom zakładowym ZTM i
&#8222;Moreny&#8221; była skomunikowana z centrum Gdańska (wcześniej, a także później jedyną możliwością dostania się
do centrum były autobusy PKS kursujące ulicą Kartuską).</p>

<p>Mieszcząca się na terenie &#8222;Moreny&#8221; kuźnia działała mniej więcej do lat 80., a następnie
została zaadoptowana na stolarnię.</p> 

<p>Po kilku latach działalność SPR &#8222;Morena&#8221; zaczęła być ograniczana. Plantacje truskawek i porzeczek zostały
zlikwidowane, część budynków została odsprzedana innym firmom (m. in. budynek nr 50 zajęła firma <strong>TRAS</strong> z Krakowa, producent
okien), aż wreszcie w roku 2009 &#8222;Morena&#8221; całkowicie zniknęła z Kiełpinka (niekoniecznie została zlikwidowana,
być może spółka jako taka istnieje do dziś).</p>

<h5>Obecne firmy i zakłady produkcyjne</h5>

<p>Nieco wcześniej, w 1996 roku, firma <strong>Femax</strong> odkupiła zabudowania po Zakładach Transportu Mięsnego i 2 czerwca 1997 roku
otworzyła swoją hurtownię. W 1998 roku natomiast na terenach należących do SPR &#8222;Morena&#8221; wybudowano hipermarket 
<strong>Auchan</strong>, którego otwarcie nastąpiło 23 marca. W związku ze sprzedażą terenu &#8222;Moreny&#8221; wyburzone zostały budynki leżące między 
ul. Szczęśliwą a obwodnicą, czyli pieczarkarnie (dawne świniarnie), kotłownia, parownia itp.</p>

<p>Budynki nr 46 i 48 oraz kuźnia należą obecnie do firmy Promotor. Pomieszczenia w nich są wynajmowane, m. in. firmie
DMT Systems oraz firmom związanym z przebudową Węzła Karczemki.</p>

<h5>Nowe budownictwo</h5>

<p>W roku 1999 grupa inwestycyjna Hossa rozpoczęła budowę osiedla <strong>Wiszące Ogrody</strong>. W roku nie wiadomo
którym <span class="sprawdzic">(ustalić datę)</span> powstało również osiedle domków jednorodzinnych. W roku 2010
powstał sklep sportowy <strong>Decathlon</strong> i rozpoczęto przebudowę Węzła Karczemki oraz ostatniego odcinka
<strong>Trasy W-Z</strong>. W roku 2011 rozpoczęto przygotowania do budowy <strong>Pomorskiej Kolei Metropolitalnej</strong>.</p>

<h5>Pozostałe informacje</h5>
 
<p>Przez pewien czas (około pięciu lat) w Kiełpinku istniał sklepik spożywczy w budynku nr 48.</p>

