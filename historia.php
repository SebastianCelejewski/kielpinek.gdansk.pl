<h3><a name="koncepcje_urbanistyczne">Koncepcje urbanistyczne</a></h3>

<div class="k">
<p>Uchwała Rady Miasta Gdańska nr XL/277/92 z 18 lutego 1992 zdefiniowała następujące funkcje Kiełpinka:
<ul>
<li>funkcja mieszkaniowa (4 - 5.5 tys. mieszkańców),</li>
<li>autoport (obsługa obwodnicy),</li>
<li>korytarz ekologiczny wzdłuż potoku Jasień.</li>
</ul></li>
</div>

<div class="k">
<p>W 1993 roku Kiełpinek został objęty dwoma planami zagospodarowania przestrzennego:
<ul>
<li>uproszczonym miejscowym planem zagospodarowania przestrzennego (uchwała Rady Miasta Gdańska z 15 czerwca 1993 r.),</li>
<li>miejscowym planem ogólnym zagospodarowania przestrzennego (uchwała Rady Miasta Gdańska z 29 czerwca 1993 r.).</li>
</ul>
</p>
</div>

<div class="k">
<p>Według <strong>miejscowego planu ogólnego</strong> Kiełpinek miał pełnić następujące funkcje:
<ul>
<li>autoport (obsługa obwodnicy),</li>
<li>przetwórstwo rolno-spożywcze i rolnictwo (w części zachodniej),</li>
<li>funkcje specjalne (w części wschodniej).</li>
</ul></p>
</div>

<div class="k">
<p>Według <strong>miejscowego planu uproszczonego</strong> Kiełpinek miał być obszarem:
<ul>
<li>wyraźnie wyodrębnionym z przestrzeni miasta mocnymi elementami granicznymi (drogami, nasypem kolejowym),</li>
<li>posiadającym strefowy układ przestrzenny (korytarz ekologiczny w części wschodniej, autoport w części zachodniej, część mieszkaniowa pośrodku),</li>
<li>zagospodarowanym w wysokim standardzie (ograniczona wysokość i intensywność zabudowy, duży udział terenów zielonych itp.),</li>
<li>posiadającym pełen zestaw usług: szkołę podstawową, dwa przedszkola, żłobek, przychodnię zdrowia, aptekę, urząd pocztowy, bibliotekę, komisariat lub
posterunek policji.</li>
</ul></p></div>

<p>W dokumentach z tego okresu nie występuje obecne centrum handlowe, pojawia się za to tzw. autoport. Jest to miejsce obsługi ruchu ciężarowego na potrzeby
portu i miasta oraz miejsce obsługi podróżnych poruszających się Obwodnicą Trójmiasta (nazywaną nie wiadomo dlaczego &#8222;autostradą&#8221;). Oprócz tego
silnie obecne jest przetwórstwo i rolnictwo reprezentowane przez Rolniczą Spółdzielnię Produkcyjną &#8222;Morena&#8221;, a pod określeniem &#8222;funkcje
specjalne&#8221; kryje się prawdopodobnie poligon wojskowy.</p>

<p>Z ciekawostek można jeszcze wspomnieć o tym, że ma planie dołączonym do miejscowego planu uproszczonego zaznaczono miejsca, gdzie miało powstać przedszkole (obecnie znajduje się tam sklep Decathlon) oraz przychodnia zdrowia (w miejscu obecnego budynku przy Przytulnej 22), a w tekście jest także mowa
o &#8222;rozmównicy publicznej&#8221; oraz schronach typu I.</p>

<p>W 1999 roku RSP &#8222;Morena&#8221; wraz z firmą &#8222;TRAS&#8221; złożyły wniosek o zmianę planu miejscowego. Zamiarem tych firm była zmiana
przeznaczenia kilku działek na mieszkalnictwo. W raporcie Andrzeja Baranowskiego dotyczącego tego wyniosku (patrz bibliografia poniżej) napisano, że
od czasu zatwierdzenia obowiązującego uproszczonego planu pojawiły się nowe okoliczności, mogące mieć wpływ na zmianę określonych w planach
ograniczeń i warunków zagospodarowania. Chodzi o powstanie centrum handlowego, wybudowane osiedla domków jednorodzinnych, rychłe rozpoczęcie budowy
osiedla Wiszące Ogrody, zlikwidowanie przemysłu rolno-spożywczego przez &#8222;Morenę&#8221; itp.</p>

