<p>Okolice obecnej bramy wjazdowej do Femax, rok 1981 (nadesłane i opisane przez Iwa6868).</p>

<p>Są to zabudowania Zakładów Transportu Mięsnego w Gdańsku, które mieściły się na terenie obecnego Femaxu.</p>

<p>Na pierwszym planie niewielki budynek dyżurki. Duży prosty budynek z tyłu mieścił warsztaty samochodowe.
Po prawej budynek, w którym obecnie mieści się Femax.</p>

<p>Są to pozostałości po majątku. Pałac znajdował się po lewej stronie patrząc na zdjęcie, dokładnie naprzeciwko budynku nr 44.
W budynku tym podobno mieszkał zarządca majątku. Krótko przed wojną pałac i część budynków zostały zniszczone w wyniku pożaru.
Budynek, w którym jest hurtownia Femax przetrwał; prawdopodobnie był to kurnik lub obora.</p>

<p>Przetrwały również dalsze budynki należące do tego majątku, znajdujące się na placu po drugiej stronie ulicy Szczęśliwej.
Wybudowano je podobno krótko przed pożarem i wybuchem wojny, dlatego zachowały się w tak dobrym stanie. 
Budynek nr 46 to świniarnia nr 1, nazywany &#8222;jedynką&#8221;.
Budynek nr 48 pełnił rolę spichlerza i stodoły.
Znajdujący się między nimi mniejszy budynek obok to dawna kuźna, czynna do lat 80. XX w., która później służyła jako stolarnia
SPR ,,Morena''.</p>

<p>Dla orientacji: numeracja posesji przy ulicy Szczęśliwej. Na terenie Femaxu pozostałością po majątku jest budynek znajdujący
się najbliżej obwodnicy, z niebieskim dachem.</p>

