<a name="mapy_wspolczesne"></a>
<h3>Mapy współczesne</h3>

<p><b>1. Google Maps (luty 2011 r.)</b></p>

<p><img src="/media/maps/actual/mapa-googlemaps-01.png" alt="mapa Google Maps luty 2011" width="700px"></img></p>

<p>Bardzo ciekawa mapa z licznymi elementami science-fiction: nieistniejące ulice Pomyślna, Przyjazna i Marzeń, 
nasyp kolejowy jako ulica Dolne Migowo, droga przez poligon jako Jasieńska, droga przez las (zakaz wjazdu samochodami, 
notabene istniejąca na wielu mapkach Hossy) itp. Natomiast Wiszące Ogrody reprezentowane są przez zaledwie cztery 
budynki, czyli jest to stan sprzed kilku lat. Ponadto nie ma Auchan, ale jest za to agencja podróży i turystyki 
&#8222;Holidays&#8221;.</p>


<p><b>2. Zumi (luty 2011 r.)</b></p>

<p><img src="/media/maps/actual/mapa-zumi-01.png" alt="mapa Zumi luty 2011" width="700px"></img></p>

<p>Mapa bez elementów science-fiction, ale mocno niekompletna (brak WO III). Jest to jedyna mapa, na której prawidłowo 
pokazano, że ulica Przytulna nie łączy się ze Szczęśliwą za osiedlem domów jednorodzinnych.</p>

<p><b>3. GIS Gdańsk (luty 2011 r.)</b></p>

<p><img src="/media/maps/actual/mapa-gis-01.jpeg" alt="mapa GIS Gdańsk luty 2011" width="700px"></img></p>

<p>Mapa Systemu Informacji Przestrzennej Gdańska (GIS Gdańsk). Jest dość dobrze opracowana i zawiera mnóstwo szczegółów
(m. in. numery budynków, oznaczenia działek itp.). Budynki zaznaczone są poprawnie, ale gorzej z ulicami: fragment
Szczęśliwej oznaczony jest jako Przytulna, Wiszące Ogrody III w ogóle nie posiadają jakichkolwiek ulic, a mityczne
ulice Pomyślna i Przyjazna krzyżują się wewnątrz hipermarketu Decathlon.</p>

<p><b>4. Trójmiasto.pl (luty 2011 r.)</b></p>

<p><img src="/media/maps/actual/mapa-trojmiasto.pl-01.png" alt="mapa Trojmiasto.pl luty 2011" width="700px"></img></p>

<p>Kolejny raz nieistniejące ulice Pomyślna i Przyjemna wygrywają z niedorysowaną Przytulną. Brakuje także odcinka 
Szczęśliwej wiodącej do byłej stacji kolejowej.</p>

<p><b>5. Targeo.pl (maj 2011 r.)</b></p>

<p><img src="/media/maps/actual/mapa-targeo.pl-01.png" alt="mapa Targeo.pl maj 2011" width="700px"></img></p>

<p>Ciekawostką mapy są zaznaczone bryły niektórych budynków, zaletą zaznaczenie jednokierunkowych ulic, brak 
elementów science-fiction i rozróżnienie między drogami normalnymi a gruntowymi. Wadą jest brak połowy osiedla.</p>

